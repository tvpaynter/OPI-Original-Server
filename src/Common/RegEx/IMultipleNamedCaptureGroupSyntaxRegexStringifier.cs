namespace StatementIQ.RegEx
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Values that represent named capture group bracket options. </summary>
    /// <remarks>   StatementIQ, 5/14/2020. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public enum NamedCaptureGroupBracketOption
    {
        /// <summary>   An enum constant representing the angle bracket option. </summary>
        AngleBracket,

        /// <summary>   An enum constant representing the apostrophe option. </summary>
        Apostrophe
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Interface for multiple named capture group syntax RegEx stringifier. </summary>
    /// <remarks>   StatementIQ, 5/14/2020. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public interface IMultipleNamedCaptureGroupSyntaxRegexStringifier : IRegexStringifier
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the named capture group bracket option. </summary>
        /// <value> The named capture group bracket option. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        NamedCaptureGroupBracketOption NamedCaptureGroupBracketOption { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets a value indicating whether this supports angle bracket naming. </summary>
        /// <value> True if supports angle bracket naming, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        bool SupportsAngleBracketNaming { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets a value indicating whether this supports apostrophe naming. </summary>
        /// <value> True if supports apostrophe naming, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        bool SupportsApostropheNaming { get; }
    }
}